package corejavaone;

public class DataTypeExample {

	public static void main(String[] args) {
		byte a = 10;
		short b = 100;
		int c = 200;
		long d = 300;
		float e = 8.7f;
		double f = 45.2344;
		char g = '!';
		boolean h = true;
		String i = "Hello";
		System.out.println("Byte Type : "+a);
		System.out.println("Short Type : "+b);
		System.out.println("Int Type : "+c);
		System.out.println("Long Type : "+d);
		System.out.println("Float Type : "+e);
		System.out.println("Double Type : "+f);
		System.out.println("Character Type : "+g);
		System.out.println("Boolean Type : "+h);
		System.out.println("String Type : "+(i+'A'));
	}

}
